			 মেকানিক্যাল ইঞ্জিনিয়ারিং বা যন্ত্রকৌশল পৃথিবীর অন্যতম বৃহত্তম ও বিস্তৃত ইঞ্জিনিয়ারিং ক্ষেত্র। অনেকে মজা করে একে “যন্ত্রনা”কৌশলও বলে থাকে  :Pএটার পক্ষে যুক্তি হলো “ভালো জিনিস অর্জন সহজসাধ্য নয়”<br>
		মেকানিক্যাল নিয়ে সবচেয়ে প্রচলিত রিউমার গুলোর মধ্যে অন্যতম হলো মেকানিক্যাল কঠিন + দেশে মেকানিক্যালের চাকরী নাই।
		কোন ইঞ্জিনিয়ারিং ই সোজা না। শুধু শুধু মেকানিক্যাল কে দোষ দিয়ে লাভ আছে? তবে এটা ঠিক মেকানিক্যালে অনেক বেশি টপিক পড়তে হয়, অন্যদের তুলনায় একটু বেশি চিন্তা ভাবনা মাথার ভেতর রাখতে হয়। ১২ বছর পড়াশোনা করার পর যারা এই পর্যন্ত আসছ, তাদের জন্য এইটুকু করা কঠিন কিছু না।
		চাকরির ব্যাপারে যেটা বলবো, সেটা হলো, শোনা কথায় কান না দিয়ে জব প্রোভাইডারদের সাইট গুলোতে একটু ঘুরে আসো। অধিকাংশ চাকরিতে তে লেখা BSc in ME/EEE বা BSc in ME/IPE মানে, মেকানিক্যাল পড়লে তুমি অনেক EEE + IPE রিলেটেড জব এও এপ্ল্যাই করতে পারবা। এইটা একটা advantage। দেশে মেকানিক্যালের চাকরি নাই, কথা টা ১০০% ভুল। আর, বিদেশে তো সোনায় সোহাগা।<br>
		“২০০৯ সালের হিসাব মতে যুক্তরাষ্ট্রে প্রায় ১৬ লক্ষ প্রকৌশলী কর্মরত আছেন। তারমধ্যে প্রায় ২৩৯০০০ (১৫%) যন্ত্র প্রকৌশলী যা পুরকৌশলীদের(২৭৮০০০) পর দ্বিতীয় সর্ব বৃহৎ বিভাগ। ২০০৯ সালের পরিসংখ্যান মতে প্রতি দশকে ৬% হারে যন্ত্রপ্রকৌশলীদের কর্মসংস্থান বাড়ছে, যাতে প্রত্যেক স্নাতক ডিগ্রীধারী প্রকৌশলীদের প্রাথমিক বেতন হল প্রতি বছরে ৫৮,৮০০ মার্কিন ডলার । যন্ত্রপ্রকৌশলীদের গড় বেতন প্রতি বছরে ৭৪,০০০ মার্কিন ডলার, যা সর্বোচ্চ প্রতি বছরে ৮৬,০০০ মার্কিন ডলার এবং সর্বনিম্ম ৬৩,০০০ মার্কিন ডলার”<br>
		(ওপরের এই প্যারাটুকুর দায়দায়িত্ব উইকিপিডিয়ার - http://bn.wikipedia.org/wiki/যন্ত্র_প্রকৌশল)
