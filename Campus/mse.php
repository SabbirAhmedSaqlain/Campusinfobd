		 <h2 id="subject_name">Material Science and Engineering</h2>
		 <br><br>
		 <p id="subject_details">

	 আধুনিক যুগের শুরুতে মানুষ যখন ইলেক্ট্রনিক ডিভাইস এবং কম্পিউটার আবিস্কার করল, তখন কম্পিউটার এর আকার কত বড়ছিল,তা আমরা সবাই জানি। এ সমস্যা থেকে উঠে আশার জন্য বিজ্ঞানীরা তখন কম্পিউটার এবং এই ইলেক্ট্রনিক ডিভাইস তৈরি কারক পদার্থ নিয়ে কাজ করা শুরু করল ।তখনই যাত্রা শুরু করল আমাদের এই Material Science and Engineering এর, ১৯৬০ এর দশকে, আমেরিকা এর Northwestern University তে । তাই বলে এই বিষয়ে যে আগে পরানই হতোনা বা এর কোন অস্তিত্তই চিল না, টা কিন্তু নয় । ছিল, কিন্তু টা এত ব্যাপক পরিসরে ছিল না । বর্তমানে এই বিষয় পড়ানো হয় MIT, Stanford , University of California , Tokyo , Singapore, Imperial College Of London ,Harvard ,Caltech ,Seoul – সহ নামিদামি World Top Rank University গুলোতে । তাই বোঝাই যাচ্ছে এর চাহিদা কোন পর্যায়ে!আমাদের রুয়েটে এবছর চালু হওয়া এই ডিপার্টমেন্টটা খুলে দিবে এক বিশাল সম্ভাবনার দ্বার । <br><br>
কথা হচ্ছে এর ক্ষেত্র বা পরিসীমা কতদূর? এখানে কি কি শেখানো হয়?<br>
আসলে এর নির্দিষ্ট কোন বাঁধা ধরা ক্ষেত্র নাই । Nano Science এর Nano Material থেকে শুরু করে বিশাল যুদ্ধ জাহাজ বা সাবম্যারিন তৈরি সকল ক্ষেত্রেই সমান ভাবে অবদান রেখে চলেছে এই MSE . অন্যান্য বিশ্ববিদ্যালয় এর পড়ানোর সিলেবাস দেখলে দেখা যায় এই বিষয়টাকে ৫ টি প্রধান ভাগে ভাগ করে এবং পরবর্তীতে এদের শাখা-উপশাখা তৈরি করা হয়েছে ।<br><br>
প্রধান শাখাগুলো হলঃ<br><br>
১। Semiconductor – প্রথমেই এই শাখাটি নিয়ে সামান্য বর্ণনা দিয়ে দিয়েছি ।আমাদের চার পাশের ইলেক্ট্রনিক ডিভাইস গুলো আরো উন্নত করা যায় কীভাবে? তাপ সহনশীল, তাপ পরিবাহী, উন্নত তড়িৎ পরিবাহী কীভাবে করা যায়?--এসব নিয়ে এখানে পড়াশুনা করা হয় । এছাড়াও পদার্থের Quantum লেভেলেও পড়াশুনা করা হয় এখানে । <br><br>
২।Metal – এখানে মূলত যে বিষয়টা মাথায় রাখা হয়,তা হল ধাতু নিষ্কাশনের উপায়গুলোর উন্নতিকরণ । এছাড়াও সঙ্কর ধাতু তৈরিকরণ, রিসাইক্লিং ,জাহাজ , প্লেনের বডি তৈরিকরণসহ আর নানান ধাতু জাত বিষয়ে পড়ান হয় । <br><br>
৩। Ceramic – আমাদের বাসায় প্রতিদিন প্রয়োজন পরে এমন বস্তু গুলো যেমন – সিরামিকের প্লেট, বাসার টাইলস, optical fiber, semiconductor, insulator -সব জায়গাতে এই বিষয়ের প্রয়োজনীয়তা অনেক বেশি । <br><br>
৪। Polymer – চারপাশের প্লাস্টিক সম্বলিত যা কিছু দেখা যায়, তার প্রায় সবই পলিমার । মোবাইলের কভার থেকে কীবোর্ড এর key গুলো পর্যন্ত – সবই পলিমার । <br>
৫। Composite – উপরের সব গুলো পদার্থ নিয়ে যা তৈরি করা হয়, তাই এই composite, । এখানে জাহাজ এর বডি কি ভাবে উন্নতি করা যায়, বুলেট প্রূফ জ্যাকেট কি ভাবে তৈরি করা যায়, খেলনার body, heavy duty factory gloves তৈরিকরণ- সহ আর অনেক কিছুই এখানে পড়ানো হবে,এদের ধর্ম, এদের উৎপত্তি , এদের সব কিছু বিশদ ভাবে পড়ানো হবে ।<br><br>
