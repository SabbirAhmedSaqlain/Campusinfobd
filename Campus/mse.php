Aerospace, Armed Forces & defense, Nuclear Industry, Oil & Gas, Pharmaceuticals ইত্যাদি সেক্টরে MSE শিক্ষার্থীদের চাহিদা প্রচুর । বাংলাদেশের প্রেক্ষাপটে এদের চাহিদা আরও ব্যাপক ।বাংলাদেশে Gas , Coal , Pharmaceuticals ইত্যাদি ক্ষেত্রে এদের অনেক চাহিদা । সম্প্রতি তৈরি হওয়া “রূপপুর পারমাণবিক কেন্দ্রে” MSE Engineer এর চাহিদা আরও বেড়ে যাবে ।এছাড়া Nanotechnology, Bio medical, polymer science ইত্যাদি ক্ষেত্র যা বাংলাদেশে এখন সেভাবে প্রতিষ্ঠিত হয়নি,এই সব সেক্টরে সৃজনশীলতাকে কাজে লাগিয়ে বিপ্লব ঘটিয়ে দেয়া সম্ভব।যারা বাহিরে যেতে চায় তাদের জন্য অনেক সুযোগ রয়েছে । রাশিয়া,জাপান,কানাডা,আমেরিকা ইত্যাদি দেশে MSE Engineer দের চাহিদা আকাশছোঁয়া ।
তাই যদি থাকে সৃজনশীলতা,অদম্য ইচ্ছাশক্তি,Nano Particle এর চেয়েও ক্ষুদ্র ক্ষুদ্র জিনিস নিয়ে গভীর চিন্তাশক্তি তাহলে, MSE এক অভাবনীয় সম্ভাবনা হতে পারে।<br><br>
