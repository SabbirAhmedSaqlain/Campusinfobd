		 <h2 id="subject_name">Aeronautical Engineering</h2>
		 <br><br>
			 <p id="subject_details">

				এক সময় আকাশে ওড়ার বিষয়টি মানুষের কাছে শুধু স্বপ্ন ছিল। মানুষের সেই স্বপ্ন পূরণ হয় ১৯০৩ সালে। সেই ইতিহাস সবারই জানা। সেই অর্থে অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং এর বয়স ১০০ বছর। বয়স যতই হোক, এটি বিশ্বের সবচেয়ে গুরুত্বপূর্ণ এবং জনপ্রিয় বিষয়ের মধ্যে একটি। অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং এ মূলত বিমানের গঠন, উৎপাদন এবং পরিচালনার বিষয়গুলো পড়ানো হয়। যাত্রীবাহী বিমান থেকে শুরু করে মহাকাশের নভোযানগুলোর তৈরি কৌশল সবই এই বিভাগের অন্তর্গত। আর তথ্য ও প্রযুক্তির এই যুগে এটি যে কতটা গুরুত্বপূর্ণ তা আশা করি সবাই বুঝবে।<br><br>
				বাংলাদেশের প্রেক্ষিতে এই subject টি এখনও সদ্য জন্মপ্রাপ্ত শিশুর মতো। অনেকেই হয়ত এই বিষয়টির নাম এখনও শোনেই নি । বাংলাদেশে সর্বপ্রথম MIST, ২০০৯ সাল থেকে অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং চালু করে। চালু করার প্রধান কারণটি ছিল বাংলাদেশের Aviation সেক্টরে যে অনগ্রসরতা, তা কাটিয়ে ওঠা।<br><br>
				তোমরা একটা কথা শুনলে অবাক হবে যে , বোয়িং বাংলাদেশের ডিসি-১০ বিমান ওদের জাদুঘরে রাখবার জন্য চেয়েছে,কারণ এই ডিসি-১০ বিমান বিশ্বজুড়ে পরিত্যক্ত ঘোষণা করা হয়েছে। এর দ্বারা তোমরা সহজেই বাংলাদেশের Aviation সেক্টরের অবস্থা উপলব্ধি করতে পারবে। তাই দেশের Aviation সেক্টরকে যারা সমৃদ্ধ করতে চায়, আকাশকে যারা সঙ্গী হিসেবে পেতে চায় তারা এই বিভাগে most welcome !
				Subject টি মূলত ২ ভাগে বিভক্ত। একটি Aerospace অপরটি Avionics । এই দুটি discipline নিয়ে সংক্ষেপে নিচে বর্ণনা করা হল:
				Aerospace: Aerospace Engineering এ বিমানের মেকানিক্যাল অংশ নিয়ে পড়ানো হয়। এই বিভাগের আলোচ্য বিষয়গুলো হল:<br><br>
				1. Aerospace Propulsion<br>
				2. Applied & High Speed Aerodynamics<br>
				3. Aerospace Vehicle Design<br>
				4. Rotorcraft Performance <br>
				5. Weapons Engineering<br>
				6. Aircraft Structural Design<br>
				7. Aircraft Loading & Structural Analysis<br>
				8. Space Engineering etc.<br><br>
				Avionics: Avionics এ বিমানের ইলেক্ট্রিক্যাল অংশ পড়ানো হয়। এই বিভাগের আলোচ্য বিষয়গুলো হল:<br><br>
				1. Avionics Engineering<br>
				2. Radar Engineering<br>
				3. Satellite Communication<br>
				4. Optoelectronics <br>
				5. Optical Fiber Communication<br>
				6. Microwave engineering <br>
				7. Aero-measurement & Instrumentation<br>
				8. Guidance, Navigation & Control etc<br><br>
				অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং পড়ে তুমি মেক্যানিকাল, ইলেক্ট্রিক্যাল সহ প্রায় সব ইঞ্জিনিয়ারিং এরই স্বাদ পাবে, কারন অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং “প্রায় সবকিছু” নিয়েই আলোচনা করে।<br>
				বিশ্বের প্রায় ৬২টি দেশে অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং পড়ানো হয়। এ দেশ গুলোর মধ্যে অন্যতম হল China, USA, UK, Russia, Italy, Germany, Canada<br>
				এশীয় দেশগুলোর মধ্যে China ও India তে সবচেয়ে বেশি বিশ্ববিদ্যালয়ে অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং পড়ানো হয়। এছাড়াও Japan, Indoneshia, Korea, Malaysia প্রভৃতি দেশ সমূহে অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং ব্যপকভাবে জনপ্রিয়।<br>
				বাংলাদেশ ও বিশ্বের অন্যান্য দেশে এর চাহিদা খুবই বেশি। বাংলাদেশে অ্যারোনটিক্যাল ইঞ্জিনিয়ারদের স্বল্পতার কারণেই airlines কোম্পানিগুলো বর্তমানে দেশের বাইরের ইঞ্জিনিয়ারদের দিয়ে কাজ চালাচ্ছে। তাই এই বিভাগের শিক্ষার্থীরা airlines কোম্পানিগুলোতে চাকরি নিতে পারবে। এছাড়াও দেশের বাইরের airlines কোম্পানিগুলোতেও চাকরি করতে পারবে। আর একটি কথা অ্যারোনটিক্যাল ইঞ্জিনিয়াররা হচ্ছে বিশ্বের “Most Highly Paid” ইঞ্জিনিয়ার । একটি উদাহরণ দিলে পরিস্কার ভাবে বুঝতে পারবে, NASA তে ১জন অ্যারোনটিক্যাল ইঞ্জিনিয়ারের মাসিক বেতন ৯৭,৪১১ US Dollar এবং বোয়িং এ বার্ষিক বেতন ৭০,০২৩ US Dollar, যা বাংলাদেশী টাকায় কত তা তোমারা হিসেব করে বের করো । আর, দেশীয় এয়ারলাইন্স গুলোতে বেতন অন্যান্য ইঞ্জিনিয়ারিং শাখার তুলনায় মোটামোটি বেশ ভালো অঙ্কেরই বলা চলে।<br><br>
				একটা প্রশ্ন সবার মনে আসতেই পারে যে,বাংলাদেশে যেহেতু অ্যারোনটিক্যাল ইঞ্জিনিয়ার নেই, তাহলে এই বিষয়টি পড়াচ্ছে কারা? এই প্রশ্নের উত্তরে আমি বলব - MIST তে অ্যারোনটিক্যাল ইঞ্জিনিয়ারিং বিভাগটির Faculty member হচ্ছে ‘Bangladesh Air Force` এর ইঞ্জিনিয়াররা এবং এই বিভাগকে আরও সমৃদ্ধ করতে Indian Air Force এর ২ জন ইনস্ট্রাক্টর এখানে কর্মরত আছেন।
				তাই আকাশকে চ্যালেঞ্জ জানাতে চাইলে অ্যারোনটিক্যাল ইঞ্জিনিয়ারিংকে ক্যারিয়ার হিসেবে বেছে নিতে পারো ।
