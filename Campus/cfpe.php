		 <p id="subject_details">

			 CHEMICAL & FOOD PROCESS ENGINEERING সংক্ষেপে চার অক্ষর দিয়ে লিখলে সেটা হয় CFPE । 
		নতুন ধারার ইঞ্জিনিয়ারিং এর সাথে তাল মেলাতে গিয়ে যার আগমন বাংলাদেশে । <br>
		ছোটবেলা থেকে যদি তোমার স্বপ্ন থাকে তুমি এমন একটা পরিপার্শ্বে থাকবে যেখানে " চার্লি এন্ড দ্যা চকলেট ফ্যাক্টরি " মুভির মতো চারদিকে চকলেট কিংবা আইসক্রিম ভর্তি থাকবে , কিংবা বড় হয়ে দেখা Breaking bad টিভি সিরিজের Heisenberg এর মতো methamphetamine প্ল্যান্ট খুলে বসবে  , তাহলে তোমার জন্যই মাননসই একটা সাব্জেক্ট CHEMICAL & FOOD PROCESS ENGINEERING ।<br>
		মূলত chemical engineering আর food process engineering এই দুইটি engineering এর সমন্বয়ে CFPE । আর একজন CHEMICAL & FOOD PROCESS engineer এর কাজই হচ্ছে এই দুই engineering এর প্রয়োগ ঘটিয়ে বিভিন্ন সমস্যার সমাধান করা ।<br>
		এক্ষেত্রে অনেকের মনেই একটা বিভ্রান্তির সৃষ্টি হয় যে কেমেস্ট্রি আর ক্যামিকেল ইঞ্জিনিয়ারিং হয়তো একই জিনিস । আসলে এটি একটি সম্পূর্ন ভ্রান্ত ধারনা । একজন ক্যামিকেল ইঞ্জিনিয়ার কম সময়ে কম খরচে এবং পরিবেশবান্ধবভাবে বড় বড় ইন্ড্রাস্ট্রিতে কিভাবে Raw materials থেকে Final product process করে বের করতে হয় সেই কাজই করে থাকে । আর এর জন্য Engergy balance, mass balance ,thermodynamics , chemical knitices এর জ্ঞানকে কাজে লাগিয়ে বিভিন্ন unit operations যেমন - evaporation, distillation , Fluid flow , Gas absorption , এর মাধ্যমে বিভিন্ন প্রোডাক্ট প্রসেস করে Chemical , gas ,energy & oil industry , polymer synthesis , nuclear reactor প্লান্টে । তাছাড়া বিভিন্ন ক্যামিকেল প্ল্যান্ট ডিজাইন , রিয়্যাক্টর এবং প্রোডাকশন প্রসেস ডিজাইন , ফুয়েল সেল , বায়োটেকনোলজি , এনভারমেন্টাল ইঞ্জিনিয়ারিং, ন্যানো টেকনোলোজি , নিউক্লিয়ার ফিশন-ফিউশন কাজে লাগিয়ে বিদ্যুত কিংবা নিউক্লিয়ার বোমা বানানোর মতো গুরুদায়িত্বও পালন করা লাগে এই বিষয়ে পাশ করা ইঞ্জিনিয়ারের ।তবে ইন্ডাস্ট্রির প্রোডাক্ট প্রসেসিং ই এদের অন্যতম কাজ সে জন্য অনেক দেশেই কেমিক্যাল ইঞ্জিনিয়ারিং কে কেমিক্যাল প্রসেস ইঞ্জিনিয়ারিং ও বলা হয়।<br><br>
		পৃথিবীতে যতদিন ইন্ডাস্ট্রি থাকবে ততদিন কেমিক্যাল ইঞ্জিনিয়ার দের প্রয়োজন রয়েছে । ইউরোপ আমেরিকাতে যেখান ১৮ শতক থেকে ইন্ডাস্ট্রিয়ালাইজেশন¬ শুরু হয়ছে সেখানে বাংলাদেশ সহ আশে পাশের এশিয়ার দেশগুলো ইন্ডাস্ট্রিমুখি হচ্ছে দিন দিন অর্থাৎ কেমিক্যাল ইঞ্জিনিয়ার দের চাহিদা বাড়ছে সময়ের সাথে সাথে ।<br>
		অন্য দিকে ফুড প্রসেস আধুনিক বিশ্বের অন্যতম প্রয়োজনীয় সেক্টর,।<br>
		মানুষ এখন কর্মক্ষেত্রে ব্যস্ত থাকার দরুন তার খাবার তৈরীর জন্য বাড়তি সময়ের যোগান দিতে পারছে না তাই তাদের প্রয়োজন হয় পর্যাপ্ত নিউট্রিশন সমৃদ্ধ ইন্সট্যান্ট রেডি ফুড যা ইউরোপ আমেরিকা সহ উন্নত দেশগুলোর মানুষের খাবারের যোগান দেয়,
		বাংলাদেশেও এটার প্রচলন ক্রমেই দেখা যাচ্ছে,
		আর কোয়ালিটি কন্ট্রোল করে পর্যাপ্ত নিউট্রিশন সমৃদ্ধ খাবার তৈরীর কাজ টাই করে মূলত ফুড প্রসেস ইঞ্জিনিয়ার । এজন্য তিনি food property, food chemistry, food value , food preservation , microbiology , thermodynamics এর জ্ঞানকে কাজে লাগিয়ে বিভিন্ন unit operations যেমন - material handling, size reduction, pumping, cooling, forming, packaging এর ব্যবহার ঘটিয়ে caramelisation , destination, coagulation , aeration , pasteurization, এর মাধ্যমে raw milk থেকে chocolate, butter, candy, ice cream কিংবা হালের juice , beverage drinks , preserved food items উৎপাদন কাজ নিয়ন্ত্রন করেন ।<br>
		এতোক্ষন তো শুধু কি কি পড়া লাগে তার লম্বা কাহিনী বললাম । তো পড়ালেখা শেষে নিশ্চয়ই একটা ভালো চাকুরি লাগবে , অনেকে আবার উচ্চ শিক্ষার জন্য বিদেশে ফুড়ুৎ করে বিমানে চড়ে রওনা দিবে । কেননা উন্নত দেশগুলোতে chemical & food process ইঞ্জিনিয়ারদের যথেষ্ট চাহিদা রয়েছে সাথে । আর এক্ষেত্রে u.s bureau of labor statistics এর মতে U.S.A এর একজন chemical engineer এর বার্ষিক সম্মানী $102,270 এবং food process engineer এর $92,000 <br><br>
