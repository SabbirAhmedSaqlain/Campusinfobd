		Power সেক্টরের ব্যাপারটা মনে হয় সবাই এমনিতেই বুঝতে পেরেছে। যারা বিদ্যুত উৎপাদন আর সরবরাহের দায়িত্বে থাকে। এই সেক্টরের চাহিদা বাংলাদেশের প্রেক্ষাপটে এই মুহুর্তে প্রচুর। চাহিদা পুরনের জন্য প্রতি বছরই নতুন নতুন বিদ্যুত কেন্দ্র তৈরি হচ্ছে। ভবিষ্যতে নিউক্লিয়ার পাওয়ার প্ল্যান্ট স্থাপনের পরিকল্পনাও চলছে সরকারের উচ্চ পর্যায়ে। আর এই সেক্টরের পুরো দায়িত্বই থাকে ইলেক্ট্রিকাল ইঞ্জিনিয়ারদের হাতে। যারা নিজেকে একজন Power management engineer হিসেবে দেখতে চাও, তারা নিঃসঙ্কোচে EEE নিতে পারো।<br><br>
		Electronics নিয়েও সবাই হালকা পাতলা কিছু জানে। Resistor, Capacitor, Inductor এসবের নাম তোমরা শুনে এসেছ। এখনকার সময়ে তোমরা যত ডিভাইস দেখ, তার বেশিরভাগই ইন্টিগ্রেটেড সার্কিট দিয়ে ডিজাইন করা। স্মমার্টফোন, আইপ্যাড, নোটপ্যাড, ল্যাপটপ সবগুলোই ইলেক্ট্রনিক্সের অবদান। Microcontroller এর নাম হয়তো কেউ কেউ শুনে থাকবে। যা রোবটিক্স নিয়ে পড়াশোনার ব্যাপক ইচ্ছা যাদের, তাদের জন্য microcontroller হলো একেবারে শুরুর ধাপ। তবে হ্যা, এদিক দিয়ে আগাতে হলে অবশ্যই পরিশ্রমী আর উদ্যমী হতে হবে। যাদের উদ্ভাবনী ক্ষমতা বেশি, বিস্তর ইচ্ছা আছে এই rapidly running টপিক নিয়ে পড়াশোনা করার, তার জন্য EEE'র দরজা সদা উন্মুক্ত।<br><br>
		Telecommunication, যোগাযোগ নিয়ে কাজকারবার। মোবাইল ফোন কোম্পানিতে মাঝের কয়েক বছর ইইই ইঞ্জিনিয়ারদের বিশাল চাহিদা ছিলো। আমাদের মত দেশে নতুন নতুন নেটওয়ার্ক, টাওয়ার, কভারেজ তৈরিসহ সমগ্র সিস্টেম ম্যানেজমেন্টের দায়িত্বে আছে বিপুল সংখ্যক ইইই ইঞ্জিনিয়ার। শুধু এক Grameenphone এই এখন কাজ করছে প্রায় ৫ হাজার প্রকৌশলী, যার বেশিরভাগই ইলেক্ট্রিক্যাল এন্ড ইলেক্ট্রনিক্স ব্যাকগ্রাউন্ডের। যত নতুন নতুন টেকনোলজি আসবে, অত চাহিদা Expand করবে। যারা নিজেকে একজন প্রতিষ্ঠিত Telecommunication Engineer হিসেবে দেখতে চাও, তাদের জন্য অবশ্যই EEE.
		Computer আসলে এমন এক ব্যাপার যাকে কোন কিছু থেকেই আলাদা করা যাবে না। ইইই থেকে পড়ে পরবর্তীতে Software Engineer হয়ে গিয়েছে এমন উদাহরনও আছে অনেক। তুমি যদি প্রোগ্রামিং ভালো বোঝ, কম্পিউটারে ভালো দখল থাকে, তবে ইইই পড়েও তুমি খুব সহজেই CSE সাইডে Divert হতে পারো। যাকে যা suit করে আরকি!!!<br><br>
		ইইই পড়েছ বলে যে তুমি প্রোগ্রামার হতে পারবে না বা সফটওয়্যার বানাতে পারবে না এমন ধারণা পুরোপুরি ভুল। তোমার যদি Electronics এর পাশাপাশি সফটওয়্যার নিয়েও ঘাটাঘাটি করতে ইচ্ছে করে তাহলে তুমি সহজেই ইইই নিতে পারো। এখানে এই সুযোগ প্রচুর।<br><br>
		চাকরির বাজার নিয়ে আমি কোন কথা বলবো না। শুধু বলবো EEE হলো একটা Everlasting Subject. যতদিন পৃথিবী টিকে থাকবে অতদিন এর ডিমান্ড কমবে না। দেশে থাকতে পারলে ভালো, যদি তা সম্ভব না হয়, তোমার জন্য আছে বাইরে যেয়ে পড়াশোনা করার অফুরন্ত ক্ষেত্র। দেশে বিদেশে সবজায়গাতেই তোমার সম্মানজনক অবস্থান থাকবে। চাকরি নেই, হাজার হাজার স্টুডেন্ট হয়ে গেছে, এমন শোনা কথায় কান দিতে যেয়ো না। তোমার যদি যোগ্যতা আর মেধা থাকে, তবে EEE পড়ে তোমাকে একদিনও বসে থাকতে হবে না। তুমি ঠিকই তোমার Deserving পজিশনে যেতে পারবে।<br><br>
