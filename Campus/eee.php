		 <p id="subject_details">সবাই নাম শুনেছে। ইলেক্ট্রিক্যাল এন্ড ইলেক্ট্রনিক ইঞ্জিনিয়ারিং। তড়িৎ ও ইলেক্ট্রনিক প্রকৌশল। যা ইলেক্ট্রন নিয়ে আলোচনা করে। নামের মধ্য দিয়েই সাবজেক্ট সম্পর্কে হালকা ধারণা পাওয়া যায়। সরাসরি চলে যাচ্ছি সাবজেক্ট রিভিউতে।
		সবচেয়ে পুরনো এবং প্রচলিত ইঞ্জিনিয়ারিং বিষয়গুলোর মধ্যে ইইই অন্যতম আভিজাত্যের অধিকারী। আশেপাশের চারদিকে ইলেক্ট্রনিক্সের বিপ্লব দেখে নিশ্চয়ই এই সাবজেক্ট পড়ুয়াদের গুরুত্ব বা চাহিদা আগ বাড়িয়ে বলে দিতে হবে না? ইলেক্ট্রিসিটি ছাড়া যদি বিশ্ব অচল হয় তবে ইইই ছাড়াও তাই। ইলেক্ট্রিক্যাল ইঞ্জিনিয়ার না থাকলে বিদ্যুতের ব্যবস্থা করবে কে শুনি?<br><br>
		তোমার এইচএসসিতে প্রিয় বিষয় কি ছিলো? উত্তর যদি হয়ে থাকে Math বা Physics তাহলে তোমার EEE পড়া নিয়ে কোন ভয় নেই। তুমি নিঃসন্দেহে এখানে খুব সহজেই মানিয়ে নিতে পারবে। তোমার যদি Equation Solve করতে মজা লাগে, ফিজিক্স সেকেন্ড পেপারের বিদ্যুতের চ্যাপ্টারগুলো যদি তোমার কাছে অসহ্য না লাগে, তাহলে ইইই তোমাকে হতাশ করবে না কোনদিন। সার্কিট নিয়ে ঘাটাঘাটি করতে মজা লাগে? রোধের সমান্তরাল সন্নিবেশ, হুইটস্টোন কার্শফের অঙ্ক করতে ভালো লাগে? আবারো বলবো ইইই নাও।<br><br>
		তোমার যদি উদ্ভাবনী ক্ষমতা থাকে, আর থাকে লেগে থাকার ইচ্ছা তবে তুমি ইইই পড়ে অনেক বড় কিছু করে দেখাতে পারবে। প্রকৌশলিক invention সবচেয়ে চমকপ্রদভাবে দেখানোর উপযুক্ত জায়গা হলো ইলেক্ট্রিক্যাল এন্ড ইলেক্ট্রনিক ইঞ্জিনিয়ারিং।
		আপাতদৃষ্টিতে দেখতে যেমন মনে হয়, ইইই আসলে তেমন ছোট পরিসরের কোন সাবজেক্ট না। এর পরিধি ব্যাপক, ক্ষেত্র অসীম।
		ইইই এর সাবডিসিপ্লিনগুলো লক্ষ্য করো-<br><br>
