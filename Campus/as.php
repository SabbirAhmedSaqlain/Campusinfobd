				আমি ঢাকা বিশ্ববিদ্যালয়ের Applied Statistics Department এর সাবেক ছাত্র । মজার ব্যাপার হল যখন আমি ঢাবি তে ভর্তি পরীক্ষা দেব তখনো নিজে জানতাম না এই বিষয়টা কেমন ? যাহোক, আমার মনে হয় কোন বিষয় পড়ার আগে চিন্তা করা উচিত সেই বিষয়ে কী পড়ানো হয় এবং যা পড়ানো হয় তার জন্য কী কী Basic Knowledge থাকা জরুরী।<br><br>
				Applied Statistics এ পড়ার জন্য সবচেয়ে বেশী জরুরী হল Quantitative Analysis এর উপর দক্ষতা । আমি Mathematics না বলে Quantitative Analysis বলছি এ জন্য যে, Pure Mathematics বলতে যা বোঝায় এটা তা থেকে ভিন্ন । এছাড়া Student যদি নিজে খুব বেশী Logical না হয় তবে এ বিষয়টি পড়া তার জন্য খুব সহজ হবেনা ।<br><br> 
				আমার মতে যাদের মধ্যে এই গুনগুলো আছে কেবল তাদেরই আসা উচিত Applied Statistics এ পড়তে । তাছাড়া ISRT (Institute of Statistical Research & Training) তে প্রচুর পড়াশোনার চাপ থাকে । Teacher রা এখানে খুব বেশী Friendly. এবার আসি মূল কথায় Applied Statistics এ পড়লে Carrier কেমন হতে পারে…<br><br>
				Applied Statistics / Statistics প্রায় সকল প্রতিষ্ঠানে প্রয়োজন । BCS থেকে Bank Job সবজায়গাতেই Applied Statistics এর জয়জয়াকার । বিসিএস (সাধারন), বিসিএস (পরিসংখ্যান), প্রভাষক (পরিসংখ্যান) নামে তিনটি Field এ Applied Statistics এর ছাত্ররা বিসিএস পরীক্ষায় Apply করতে পারে যার মধ্যে শেষের দুটি কেবল তাদের নিজস্ব । অন্যরা এক্ষেত্রে কেবল দুটি Field এ Apply করতে পারে । Private Bank গুলোতে Applied Statistics এর শিক্ষার্থীদের জয়জয়াকার । Bangladesh Bank এর AD (General) এর পাশাপাশি AD (Statistics) ও AD (Research) নামে আলাদা দুটি Field আছে যাতে Statistics/Applied Statistics/Economics ই Apply করতে পারে । যেকোন Research ধর্মী প্রতিষ্ঠান মানেই Applied Statistics/Statistics যেখানে ISRT এর শিক্ষার্থীদের ভীষন চাহিদা । ICDDR,B, NIPORT, AC Neilson সহ বড় বড় Research প্রতিষ্ঠানে Applied Statistics এর জয়জয়াকার ।<br><br>
				আসল কথাতো বলাই হয়নি, BBS (Bangladesh Bureau of Statistics) এরনিয়ন্ত্রঙ্কর্তাতো আমরাই সেটা বলাই বাহুল্য । এছাড়া ফ্রিল্যান্সার হিসেবে IT এর পরে Applied Statistics ই বেশী গুরুত্বপুর্ন । তবে মনে রেখ, ISRT এর শিক্ষার্থীরা এমনি এমনি শুধু তাদের Subject এর মাধ্যমে এ পর্যায়ে এসে পৌছায়নি । এই Department এর সকল শিক্ষার্থীই স্বভাবত ভীষন অধ্যবসায়ী, ভীষন পরিশ্রমী এবং নিসন্দেহে মেধাবী । Department তাদের এমন চাপ নিতে শিখিয়েছে যে কোন কিছুকেই তারা চাপ মনে করেনা । এমন একটি Department এ পড়ে আমি ভীষন গর্বিত । যদি প্রস্তুত থাক ; তবে Applied Statistics এ তোমাদের স্বাগতম ।<br><br>
