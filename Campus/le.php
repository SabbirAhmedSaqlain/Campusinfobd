বাংলাদেশে কেবল মাত্র ২ টি ইউনিভার্সিটি তে B.Sc in Leather Engineering ডিগ্রী প্রদান করা হয়ে থাকে । Khulna University Of Engineering & Technology (KUET) এ (60 seats) এবং Instute of Leather Engineering & Technology (University of Dhaka) তে (LE=40 seats, FWE=40 seats, LPE=40 seats) ।কুয়েটে Leather Engineering ডিপার্টমেন্টে একইসাথে Leather Manufacturing Engineering, Leather products Engineering এবং Footwear Engineering বিষয় গুলো পড়ানো হয়। অন্যদিকে ঢাকা বিশ্ববিদ্যালয়ে Leather Engineering & Technology অনুষদে ঐ তিনটি সাবজেক্ট এ আলাদা আলাদা তিনটি department আছে ।। আমাদের দেশে ঢাকার হাজারীবাগ ছাড়াও চট্রগ্রাম এ ও leather and leather goods এর বর্তমান চাহিদার উপর ভিত্তি করে গড়ে উঠছে Leather Industry । কিন্তু এসকল industry এর জন্য যে পরিমান দক্ষ ইঞ্জিনিয়ার দরকার তা fulfill করতে পারেনি বাংলাদেশ, ফলশ্রুতিতে বিদেশ থেকে leather engineer বাংলাদেশে আনা হচ্ছে । বাংলাদেশ সরকার এই চাহিদা বুঝতে পেরে 2010 সালে কুয়েটে ৬০ আসন বিশিষ্ট Leather Engineering department খোলার অনুমোদন দেয় । চামড়া ও চামড়া জাত দ্রব্য রপ্তানিতে বাংলাদেশের Leather Industry চতুর্থ অবস্থানে (বাংলাদেশের রপ্তানি বাণিজ্যের মধ্যে )। একটা সময় ছিল যখন মানুষ tannery এর নাম শুনলে নাক শিটকে নিতো, কিন্তু সাবজেক্ট টার সাথে বিভিন্ন কেমিক্যাল term এর পাশাপাশি bio-chemical এবং bio-technological term এর সংমিশ্রণের কারণে Leather Engineering বর্তমান বহির্বিশ্বে আলোচনার কেন্দ্রবিন্দুতে ।N.B: বোঝার সুবিধার্থে কিছু English Term ব্যাবহার করা হয়েছে।<br>

