		 <p id="subject_details">
	 লেদার ইঞ্জিনিয়ারিং মূলত চামড়া ও চামড়াজাত দ্রব্যকে সংশ্লেষণ,উৎপাদন এবং পরিশুদ্ধ করণের সাথে জড়িত, যার মাধ্যমে এগুলোকে বিভিন্ন বাণিজ্যিক দ্রব্য যেমনঃ Footwear,Clothing,Gl¬-oves,Bags,Pholstery¬ including Automobiles and Sports goods ইত্যাদিতে রুপান্তরিত করা যায় ।উইকিপিডিয়া অনুসারে “Leather is a durable and flexible material created by tanning of animal rawhide and skin”।অর্থাৎ ‘Leather’ বলতে শুধু মাত্র চামড়া (skin) বললে ভুল হবে। বাংলাদেশের মানুষের মধ্যে এই সাবজেক্ট টা নিয়ে একটা ভ্রান্ত ধারণা আছে। অনেকেতো একে ‘চামড়া প্রকৌশল’ বলে দাবি করে! যাই হোক আসল কথায় আসা যাক,এর manufacturing process টি বিভিন্ন ধাপের মাধ্যমে cottage industries থেকে heavy industry তে ত্বরান্বিত হয়। বাংলাদেশ সহ বিশ্বের বিভিন্ন ইঞ্জিনিয়ারিং ইউনিভার্সিটি তে পড়ানো এই Leather Engineering সাবজেক্ট টি মূলত Raw leather কে Finished leather এ synthesis করার তিনটি পর্যায় সম্পর্কে ধারণা দিয়ে থাকে শিক্ষার্থীদেরকে। এই তিনটি পর্যায় গুলো হলঃ Preparatory stages, Tanning and Crusting । এখন আসা যাক Leather Engineering বিভাগের core subject গুলো সম্পর্কে। Core subject গুলো হলঃ*Biochemistry of proteins, *principles of Pre-Tannage,*Analytical chemistry of pre –Tanning materials, *Biotechnology of Leather manufacture,*Mechanics Leather Machines, *Methods of leather manufacture,* Post tanning operations, *Analytical Chemistry of Post tanning & finishing agents,*Analytical Chemistry of leather, *Footwear Engineering and Leather products Engineering.এই বিষয় গুলো ছাড়াও চার বছরের কোর্সের এই ডিপার্টমেন্টটিতে প্রথম ২ বছরে ডিপার্টমেন্টল সাবজেক্ট এর পাশাপাশি Basic Electrical Engineering, Computer fundamental and programming , computer aided graphics design, Basic Mechanical Engineering, ইত্যাদি বিষয়গুলো সহ Inorganic and Organic Chemistry, Mathematics, Physics, Accounting, English, and Economics সাবজেক্ট গুলো পড়ানো হয়। Leather Engineering এ B.Sc Engineering পাশ করার পর শিক্ষার্থীরা Leather Engineering এ M.Sc করার পাশাপাশি Chemical Engineering এবং IPE ডিপার্টমেন্ট এও M.Sc Engineering পড়তে পারে। একজন Leather Engineering এর স্টুডেন্ট যে সকল সেক্টরে job করতে পারেঃ<br><br>

