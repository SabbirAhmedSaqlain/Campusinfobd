		 <p id="subject_details">

	 Subject Review: ইলেকট্রনিক্স ও কমিউনিকেশন ইঞ্জিনিয়ারিং (ECE) / ইলেকট্রনিক্স ও টেলিকমিউনিকেশন ইঞ্জিনিয়ারিং(ETE)/ ফলিত পদার্থবিজ্ঞান, ইলেকট্রনিক্স ও কমিউনিকেশন ইঞ্জিনিয়ারিং(APECE)<br>
৩ টা বিষয় সম্পর্কে একসাথে লিখলাম – কারণ এদের মাঝে মিল অনেক। মিলগুলো যেমন জানা জরুরি তেমনি কোন কোন জায়গায় অমিল সেটা তুলে ধরার চেষ্টা করব।<br>
বিশ্বের অন্যতম পুরনো ও র্যঙ্কিং এ প্রথমদিকের ইঞ্জিনিয়ারিং বিষয়ের নাম বললে চলে আসে EEE- এর কথা। সেই EEE এর থেকেই ECE এর উৎপত্তি । আর ইটিই হল ETE এর এ একটা অংশ ।<br>
ECE বিষয় নিয়ে আগে বলি। এই বিষয়ের Core Course হল Electronics and Communication । ইলেকট্রনিক্স সম্পর্কে আমরা কম বেশি সবাই জানি । সমস্যা হয় কমিউনিকেশন এর বেপারটা মাথায় ঢুকে না। ECE এর কোর্স এ প্রথম দিকে থাকে ব্যপকভাবে Electronics এর কোর্স। এক সময় Communication এ প্রবেশ করবা। Electronics এর জ্ঞান ছাড়া Communication সম্ভব না।<br>
একজন ছাত্র ECE তে সাধারণত যে কোর্সগুলো পরে থাকে [ কোর্সগুলোর নাম এ সামান্য বিভিন্নতা থাকে বিশ্ববিদ্যালয়ভেদে]-
1.Analog Electronics; 2. Digital Electronics; 3.Electrical Machines and Measurements; 4. Electrical Circuit Analysis; 4. Engineering Electromagnetic and Antenna Theory 5.Industrial and Power Electronics ; 6. Radio and Television Engineering; 7.Advanced Electronic Circuits; 8.Electronic Devices 9.Optical Fiber Communication; 10. Microwave and Satellite Communication; 11. Digital Signal Processing; 12. Scientific, Industrial and Bio Medical Instrumentation; 13.Semiconductor and VLSI Technology; 14. Communication Theory; 15. Advanced Communication Theory; 16. Control Engineering; 17. Mobile Cellular Communication; 18. Telecommunication Network.19. Multi media Communications 20. Image Processing
এছাড়াও Programming C/C++, Microprocessor and Assembly language, Micro Controller, Data Communication and Computer Network, Computer Peripheral and Interfacing, Computer Organization and Architecture, Renewable Energy Technology, Engineering Drawing কোর্স গুলো করতে হয় ।
এছাড়া মাইনর কোর্সগুলোর কথা না বললাম।<br><br>
তাহলে একজন ইলেকট্রিক্যাল ইঞ্জিনিয়ার এর সাথে একজন কমিউনিকেশন ইঞ্জিনিয়ারের পার্থক্যটা হল ইলেক্ট্রিক্যাল ইঞ্জিনিয়ারদের Power Plant Engineering, Power Conversion, High Voltage Engineering, Power Plant Protection, Power System Reliability, Power System Operation and Control এই কোর্সগুলো মেজর হিসেবে পড়তে হয়- যেটাকে CORE কোর্স বিবেচনা করা যেতে পারে । কিন্তু কমিউনিকেশন ইঞ্জিনিয়ারদের CORE কোর্স হল কমিউনিকেশন।
তোমাকে কাজ করতে হবে কমউনিকেশন এর কথা চিন্তা করে । তবে ইলেকট্রনিক্স ইঞ্জিনিয়ার হিসেবেও নিজেকে প্রতিষ্ঠা করা ব্যপার না।
কমিউনিকেশন বলতে আমরা যা সোজা বাংলায় বুঝি যোগাযোগ। কিন্তু যোগাযোগ কখন Electronically হবে সেটাকে ধরব ECE হিসেবে। তোমার একটা তথ্য আছে সেটা আরেক জায়গায় Transfer করতে হবে। একজন ইঞ্জিনিয়ার হিসেবে কাজ হবে Data Loss/Attenuation কমানের মাধ্যমে তোমার তথ্যের সঠিক প্রেরণ । সেটার জন্য তোমাকে কখন কোথায় কি উপায় প্রয়োগ করতে হবে সেটা শিখান হবে ECE তে। একটা সময় ছিল মোবাইল এ কথা বলতে খরচ ছিল মিনিটে ১০ টাকা। এখন ১০ পয়সা। এটা সম্ভব হয়েছে টেলিকমিউনিকেশন বিভাগের উন্নতির কারনে। একসময় ছিল ইন্টারনেট ব্যবহার ছিল কল্পনার ব্যপার। এটা এখন সবার কাছে সবচেয়ে সহজ ব্যাপার। কারণ Fiber Optic Communication সহ কমিউনিকেশন এর উন্নতির কারণে।<br><br>
তবে এর মানে এই না যে তাহলে সব তো হয়েই গেছে- আমি পড়ে কি করব? মনে রাখবা একজন কমিউনিকেশন ইঞ্জিনিয়ারের কাজ কখনও শেষ হয় না। এখন পর্যন্ত Bending Loss, Dispersion এগুলো কমানো সম্ভব হয়েছে তবে অনেক Information এখনও Loss এখনো যথেষ্ট কম না। আর এসব নিয়ে কাজ এখনও হয়ে যাচ্ছে । ভবিষ্যতে আরও হবে<br><br>
ETE বিষয়টা প্রকৃতপক্ষে Mobile Communication কে বোঝায় । এখানে এটার প্রতি ভালভাবে Focus করা হায়। তবে প্রায় সব কোর্সই এক। তবে যখন একজন Satellite এর উপর কাজ করবে, NASA/ কোন কম্পানির Radar Project এ কাজ করবে তখন ECE র ই কিন্তু প্রাধান্য বেশি ।
বাংলাদেশে কুয়েটে ECE, রুয়েটে ETE, চুয়েটে ETE, ঢাকা বিশ্ববিদ্যালয়ে APECE, খুলনা বিশ্ববিদ্যালয়ে ECE, চট্রগ্রাম বিশ্ববিদ্যালয়ে APECE ও NSU te ETE ছাড়াও বেশ কিছু প্রাইভেট বিশ্ববিদ্যালয়ে বিষয়টি পরান হয় ।<br><br>
এবার ফলিত পদার্থবিজ্ঞান এর কথায় আসি। এটা পদারথবিজ্ঞান থেকে উৎপত্তি তবে বেশ দূর সম্পর্কের আত্মীয় । পদার্থবিজ্ঞান কে ইঞ্জিনিয়ারিং ফিল্ড এ Apply করার system নিয়ে কাজ করে Applied Physics. কিন্তু বলতে গেলে যদিও এখন ও বহিরবিশ্বে ৪ বছর ফলিত পদার্থবিজ্ঞান পড়ান হয় বাংলাদেশে এটা ১ বছর ও টার সামান্য বেশি পড়ান হয়- মেজর কোর্সগুলো ঠিক রেখে। বাকি ৩ বছর ECE এর উপরই focus করা হয়।<br><br>
